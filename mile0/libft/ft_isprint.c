#include <unistd.h>

int	ft_isprint(int c)
{
	if (c >= 32 && c <= 126)
		return (1);
	return (0);
}

int	main(void)
{
	int	c;
	int	i;

	c = 35;
	i = ft_isprint(c) + 48;
	write (1, &i, 1);
}
