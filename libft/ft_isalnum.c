#include <unistd.h>

int	ft_isalnum(int c)
{
	if ((c <= 122 && c >= 97) || (c <= 90 && c >= 65) || (c >= 48 && c <= 57))
		return (1);
	return (0);
}

int	main(void)
{
	char	c;
	char	i;

	c = '1';
	i = ft_isalnum(c) + 48;
	write(1, &i, 1);
}
