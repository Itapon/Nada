#include <unistd.h>

int	ft_isascii(int c)
{
	if (c >= 0 && c <= 127)
		return (1);
	return (0);
}

int	main(void)
{
	size_t	c;
	char	i;

	c = 128 ;
	i = ft_isascii(c) + 48;
	write(1, &i, 1);
}
