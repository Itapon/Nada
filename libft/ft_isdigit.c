#include <unistd.h>

int	ft_isdigit(int c)
{
	if (c >= 48 && c <= 57)
		return (1);
	return (0);
}

int	main(void)
{
	int c;
	char i;

	c = '4' ;
	i = ft_isdigit(c) + 48;
	write(1, &i, 1);
}
