#include <unistd.h>

int	ft_isalpha(int c)
{
	if ((c <= 122 && c >= 97) || (c <= 90 && c >= 65))
		return (1);
	return (0);
}

int	main(void)
{
	char	c;
	char	i;

	c = 'A';
	i = ft_isalpha(c) + 48;
	write(1, &i, 1);
}
